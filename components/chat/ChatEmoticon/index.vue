<template>
  <div class="emotion">
    <div class="emotion-header">
      <input
        ref="input"
        v-model="keyword"
        type="text"
        placeholder="找到你喜欢的表情包吧[请输入中文]"
        @keydown.enter="search"
        @input="input"
      />
      <div class="emotion-header-btn" @click="search">
        <icon icon="search" />
        搜索
      </div>
    </div>

    <div v-loading="loading" class="emotion-content">
      <emotion
        v-if="!emoticonList.length"
        :style="{ border: 'none' }"
        width="98%"
        height="100%"
        :padding="0"
        bg-color="transparent"
        @emotion="
          (emoji) => {
            $emit('emotion', emoji)
          }
        "
      />
      <div v-else class="emotion-content-emoji">
        <div v-for="(url, index) in emoticonList" :key="index" class="resulu">
          <img
            loading="lazy"
            :src="url"
            class="resulu-pic"
            @click="
              () => {
                $emit('sendEmoji', url)
              }
            "
          />
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Emotion from '@/components/base/Emotion/Emotion.vue'
import Icon from '@/components/base/Icon'

export default {
  components: { Emotion, Icon },
  data() {
    return {
      keyword: null,
      emoticonList: [],
      loading: false
    }
  },
  computed: {},
  watch: {},
  created() {},
  mounted() {},
  methods: {
    async search() {
      if (!this.keyword) return
      this.loading = true
      try {
        const res = await this.$API.chat.emoticon({ keyword: this.keyword })
        this.emoticonList = res
        this.loading = false
      } catch (error) {
        this.loading = false
      }
    },
    input(e) {
      if (!e.target.value) {
        this.emoticonList = []
      }
    }
  }
}
</script>
